#include "libft.h"

char	*ft_strnew(size_t size)
{
	char *p;

	p = NULL;
	p = (char *)malloc(size + 1);
	if (!p)
		return (NULL);
	ft_memset(p, '\0', size + 1);
	return (p);
}
